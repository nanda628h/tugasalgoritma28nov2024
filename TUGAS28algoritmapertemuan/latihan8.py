# Definisi Tuple1 dan Tuple2
Tuple1 = ("UPB", "For", "MEGAH")
Tuple2 = ("Learning", "With", "Fun")

# Gabungkan Tuple1 dan Tuple2
Tuple3 = Tuple1 + Tuple2

# Cetak Tuple pertama
print("Tuple 1: ")
print(Tuple1)

# Cetak Tuple kedua
print("\nTuple 2: ")
print(Tuple2)

# Cetak Tuple hasil penggabungan
print("\nTuples after Concatenation: ")
print(Tuple3)
